        <h3>SOME INSTRUCTIONS <span style="color: red">BEFORE</span> BLOOD DONATION:</h3>
        <p>Do not donate blood on an empty stomach.</p>
        <p>Maintain a gap of 30 min after meal.</p>
        <p>Good sleep on the previous night.</p>
        <p>Do not take alcohol 24hrs prior blood donation</p>
        <p>Do not smoke 30 min before or after blood donation.</p>
        <p>Avoid driving, vigorous exercise or playing tennis for at least 2-3hrs after blood donation.</p>
        <p>Do not donate blood if you have fever, taking antibiotics or aspirin or antihistamine or insulin.</p>

    </div>
    <div class="col-sm-6">
        <h3>SOME INSTRUCTIONS <span style="color: red">AFTER</span> BLOOD DONATION:</h3>
        <p>Eat and Drink something before leaving the blood donor area.</p>
        <p>Drink more fluid for next 4 hours.</p>
        <p>If there is bleeding from phlebotomy site raise arm and apply pressure to the site.</p>
        <p>If fainting or dizziness occur either lie down or sit with the head between the knees.</p>
        <p>If the symptom persist either telephone or returns to the blood bank or see a doctor.</p>
        <p>Resume all normal activity if asymptomatic.</p>
        <p>Remove bandage after 1 hour.</p>

