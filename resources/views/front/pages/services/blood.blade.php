<h3>WHO MAY DONATE BLOOD</h3>
<p>Blood donation is a healthy habit that helps blood renewal and it is not hazardous at all.
    The volume of blood collected from a donor is about 450ml, this volume is compensated within 24hrs of
    donation and body replace the lost red cells within several weeks. Eligible healthy donors (age 18 to 60 years,
    having Hb >12gm/dl and weight >45) male can donate blood every 4 months interval and female every 6 months
    interval. Platelet Apheresis donor’s age should be 18 to 60 years
    weight >50kg and has not taken the medicine Plavix/Ticlid for last 14 days.</p>
</div>
